public class SwitchClass {

        public static void SwitchStatement ( int switchvalue ) {
            switch(switchvalue) {
                case 1:
                    System.out.println("Value is 1 ");
                    break;

                case 2:
                    System.out.println("Value is 1 ");
                    break;
                case 3:
                case 4:
                case 5:
                    System.out.println("Value is 3,4, or 5 ");
                    break;

                default:
                    System.out.println("Value is dault");

            }

        }
    }



